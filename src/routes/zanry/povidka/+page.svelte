	<p>
		„Chytil prej osm diverzantů.“
	</p>
	<p>
		„Jo? A co to je?“
	</p>
	<p>
		Veru neodpověděla. Spočítala betonový dlaždice na cestě k domku. Pak mi poručila, abych se postavil přesně na
		vyznačený místo.
	</p>
	<p>
		„A proč?“
	</p>
	<p>
		„Neptej se pořád a dělej.“
	</p>
	<p>
		Veru se nedalo dlouho odporovat. Postavil jsem se poslušně na lajnu, kam položila klacek. Pes si nás nevšímal.
		Olizoval salám, Jako by ho ani nechtěl sníst.
	</p>
	<p>
		„A teď mi vraž facku.“
	</p>
	<p>
		„Co?“
	</p>
	<p>
		„Abys mi dal facku.“
	</p>
	<p>
		„A proč bych to dělal?“
	</p>
	<p>
		„Protože to chci.“
	</p>
	<p>
		„Já holky nemlátím.“
	</p>
	<p>
		Veru vzdychla a otočila oči v sloup.
	</p>
	<p>
		„A co když ti řeknu, že to srdce u školy jsem namalovala já?“
	</p>
	<p>
		„Blbost.“
	</p>
	<p>
		Olízla si dva prsty a zvedla ruku: „Přísahám.“
	</p>
	<p>
		Zalapal jsem po dechu.
	</p>
	<p>
		„Ty?“
	</p>
	<p>
		Přikývla.
	</p>
	<p>
		„Ale… proč bys to dělala?“
	</p>
	<p>
		„Abys už nebyl takovej vůl.“
	</p>
	<p>
		Narůstal ve mně neznámý zmatek a pocit ublíženosti.
	</p>
	<p>
		„Nejsem žádnej vůl.“
	</p>
	<p>
		„Ale jo. Ten, kdo bonzuje o přestávkách, je vůl. Všichni si to myslí. I Danihel.“
	</p>
	<p>
		Zmínka o Danihelovi zafungovala. Ruka mi najednou sama vylítla a zamířila k její tváři. Ve stejný okamžik se
		událo několik věcí zároveň. Pes se neznámou silou vymrštil z lavičky a jeho smrdutej dech mi narazil na tvář.
		Cvakly zuby. Naštěstí naprázdno. Ten neskutečenej let zastavil řetěz, na kterým byla bestie přivázaná ke dveřím.
		Verunka stála nehybně vedle mě a usmívala se.
	</p>
	<p>
		„To je, co!“
	</p>
	<p>
		Ulevilo se mi, že jsem se nepočůral.
	</p>
	<p>
		„Nesnese to.“
	</p>
	<p>
		Veru se otočila a zmizela v domě. Za okamžik jsem v patře uslyšel klavír.
	</p>
	<p>
		Obezřetně jsem obešel psa, který mi už zase nevěnoval pozornost a lízal si olysalý tlapy. Protáhl jsem se do
		chodbičky a vyletěl po schodech vzhůru, kdyby si to rozmyslel. Veru seděla u klavíru a natahovala metronom.
	</p>
	<p>
		„Myslela jsem, že ses naštval.“
	</p>
	<p>
		„A proč?“ zkoušel jsem idiotskou odpovědí získat čas. Zahrála pár akordů. Nevydržel jsem co. Popošel jsem až
		těsně za ni a ucítil mandlovej krém její matky.
	</p>
	<p>
		„Fakt jsi to namalovala ty?“
	</p>
	<p>
		„Boty!“
	</p>
	<p>
		Zahrála cosi, co znělo jako rozkaz a já si poslušně zul tenisky.
	</p>
	<p>
		„Ale já tě nemiluju. Nikoho nemiluju!“
	</p>
	<p>
		Řekl jsem to úplně bez rozpaků. Jednak jsem nevěděl, co přesně to znamená, jednak to byla pravda. Usmála se na
		mě.
	</p>
	<p>
		„Já tebe taky ne.“
	</p>
	<p>
		Ulevilo se mi.
	</p>
	<p>
		„Tak proč jsi to tam o nás napsala?“
	</p>
	<p>
		„Ale já to nenapsala o nás.“
	</p>
	<p>
		Řekla něžně, usmála se na mě a slavnostně vytloukla jakýsi osudový akord do klaviatury. Její modrý oči se opřely
		do mých a mně došlo celý to neštěstí. Na okamžik se mi rozpila v něčem, co nemohly bejt slzy. Otočil jsem se a
		jako smyslů zbavenej letěl po chodníkách nadzdvihanejch devětsilem směrem ke škole. Přeskočil jsem zářivý srdce
		E + V a vletěl do hlavních dveří. Hospodářka mě dovedla do třídy, kde jsem jen mlčel a nedokázal soudružce
		odpovědět na to, kde jsem byl.
	</p>
	<p>
		O hlavní přestávce jsem nevstal a nešel k tabuli, abych zapisoval. Něco ve mně se pokazilo. Rozpustilo v
		benzínu. Danihel to pokazil. Ten Danihel, co na mě pokřikoval, jestli jsem si všiml, jak překrásně rozkopl dveře
		do třídy. Bylo mi to fuk. Všechno mi bylo fuk až na to, co jsem si myslel, že mi fuk není. To E přilepený k V.
		To, že Danihel byl Eduard a já byl jen obyčejnej třídní bonzák s ocucaným koncem v srdci…
	</p>
