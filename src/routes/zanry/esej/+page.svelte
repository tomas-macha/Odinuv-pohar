	<p>
		Ctižádost je něco jako žluč; žlučovitost má člověka k činnosti, nutká ho, burcuje a popohání,
		pokud se k tomu neudělá přítrž. Když se však přítrž udělá a není po jejím, žluč v člověku
		ztvrdne a působí pak zhoubně jako jed. A tak jsou ctižádostiví lidé – pokud mají před sebou
		cestu volnou a mohou se brát vpřed a stoupat – spíš horliví než nebezpeční; když jim však
		rozlet zatrhnete, objeví se v nich skrytá nespokojenost, hledí zlým okem na věci a lidi, a těší
		je nejvíc, když se všechno bortí; a to je nehorší možná vlastnost služebníka knížete či státu.
	</p>
	<p>
		Jestliže tudíž knížata užívají služeb ctižádostivých lidí, měli by je vést tak, aby ti mohli jít
		pořád kupředu a ne dělat kroky zpátky; protože se to však neobejde bez nesnází, je dobře,
		když podobné povahy nepoužijí vůbec. Neboť nebudou-li v úřadě postupovat, postarají se o
		to, aby ten úřad padl spolu s nimi.
	</p>
	<p>
		Ježto jsme však řekli, že je dobré ctižádostivé povahy nepoužívat, není-li to nezbytně nutné, je
		třeba teď také říci, v kterých případech jsou tací lidé nepostradatelní. Dobré velitele ve válce
		mít musíte, ať jsou sebectižádostivější. Neboť jejich služby vyváží vše ostatní. A voják bez
		ctižádosti je voják bez ostruh. Ctižádostivci jsou rovněž velmi užiteční při ochraně knížat před
		různým nebezpečím a zášti. Takového úkolu se totiž ujme leda někdo, kdo je jako oslepený
		holub, co stoupá pořád výš a výš, protože nevidí nic kolem sebe. Dále jsou ctižádostiví lidé
		užiteční, je-li třeba strhnout k zemi vypínavého poddaného, který by všechny zastínil. Na
		tento způsob použil například Tiberius Macrota k nastrojení Seianova pádu.
	</p>
	<p>
		Ježto jsou tedy v těchto případech ctižádostivci nepostradatelní, nutno nyní povědět, jakým
		způsobem je držet na uzdě, aby byli méně nebezpeční. Jsou méně nebezpeční, pokud jsou
		nízkého rodu, než pokud jsou urození; a pokud jsou spíše drsní než laskaví a oblíbení; a
		pokud se dostali nahoru nedávno a ještě nestihli svou velikost vychytračit a opevnit. Někdo
		přičítá knížatům za slabost, že mají své oblíbence; je to nicméně nejlepší lék proti
		ambiciózním velikánům. Když totiž vede cesta ke knížecí přízni nebo nemilosti přes
		oblíbence, nikdo nevyroste příliš vysoko. Jiný způsob, jak je pokrotit, je postavit proti nim
		jiné, kteří jsou stejně pyšní jako oni. Potom však třeba mít k dispozici rádce středu, aby se
		uchovala rovnováha věcí, protože bez toho balastu by se loď příliš kymácela. Přinejmenším
		by měl kníže podněcovat a pěstovat si pár neškodných lidí, kteří by byli na ctižádostivce něco
		jako bič. Co se tkne možnosti nahnat jim strach, že jim hrozí zhouba, může to prospět, jsou-li
		to povahy bázlivé; jsou-li to povahy neústupné a odvážné, může to urychlit jejich záměry a
		jsou pak vskutku nebezpeční. Co se tkne nastrojení jejich pádu, nelze to naráz provést
		bezpečně, zbývá jen jedna cesta, a to neustále střídat projevy nepřízně a přízně; nevědí pak, na
		čem jsou, a jsou, jako by zbloudili v lese. Z různých ctižádostí je méně škodlivá ta, jež se
		soustřeďuje na velké věci, než ta, jež lpí na všem; ta totiž do všeho vnáší zmatek a zmaří
		každou práci. Přesto je méně nebezpečný ctižádostivý člověk, který se hrne do každé práce,
		než který má mocné stoupence. Kdo chce vyniknout v kruhu schopných lidí, předsevzal si
		velký úkol; a je to věc veřejně prospěšná. Kdo však ze sebe dělá číslo mezi nulami, je
		zhoubou své doby.
	</p>
	<p>
		Čest má do sebe tři věci: je živnou půdou pro dobré skutky; umožňuje přístup ke králům a
		čelným osobnostem; a povznáší a rozmnožuje statky. Kdo má při svých ambicích před očima
		tyto cíle, je poctivý člověk; a vladař, který tyto cíle pozná u člověka, jenž se snaží prosadit, je
		moudrý vladař. Obecně platí, že si knížata a státy mají vybírat za ministry takové lidi, co dbají
		víc o své povinnosti než o své povýšení; a co rádi vykonávají svůj úřad z přesvědčení, a ne
		aby se jim honosili. Knížata také musí umět rozeznat snaživou povahu od vstřícné mysli.
	</p>
